
def f(s):
    if s < 100: # s가 10보다 작으면
        return s // 10 + s % 10 # 종료
    else:
        return s % 10 + f(s//10) # 10으로 나눈 나머지(1의 자릿값) 10으로 나눈 몫
s = int(input())
print(f(s))

